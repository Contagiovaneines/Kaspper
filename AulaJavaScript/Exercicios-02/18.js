/*
Exercício 18 - Qual é o maior valor?

Ler dois valores (considere que não serão lidos valores iguais) e escrever o maior deles.
*/

var valor1 = 10;
var valor2 = 20;

// Verifica qual é o maior valor
if (valor1 > valor2) {
    // Se valor1 for maior que valor2, exibe que valor1 é o maior
    console.log(`O maior valor é ${valor1}.`);
} else {
    // Caso contrário, valor2 é o maior
    console.log(`O maior valor é ${valor2}.`);
}
